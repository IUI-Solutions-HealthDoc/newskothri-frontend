export function getInstitutionalContent(
  kind: InstitutionalKind,
  lang: "hi" | "en"
): InstitutionalPageContent {
  if (kind === "about" && lang === "hi") return ABOUT_HI;

  if (kind === "about" && lang === "en") {
    return {
      ...EMPTY,
      kicker: "Institution",
      title: "About",
    };
  }

  if (kind === "mission") {
    return {
      ...EMPTY,
      kicker: lang === "hi" ? "संस्था" : "Institution",
      title: lang === "hi" ? "मिशन" : "Mission",
    };
  }

  if (kind === "vision") {
    return {
      ...EMPTY,
      kicker: lang === "hi" ? "संस्था" : "Institution",
      title: lang === "hi" ? "विज़न" : "Vision",
    };
  }

  return EMPTY;
}
